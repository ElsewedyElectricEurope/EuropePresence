function partnerLogoURL(name){
  const domain = PARTNER_LOGOS[name];
  if(!domain) return null;
  if(LOGO_DEV_TOKEN)       return `https://img.logo.dev/${domain}?token=${LOGO_DEV_TOKEN}&size=128&format=png`;
  if(BRANDFETCH_CLIENT_ID) return `https://cdn.brandfetch.io/${domain}?c=${BRANDFETCH_CLIENT_ID}`;
  // Interim free fallback (no signup) while Clearbit stays dead — a small
  // favicon-quality icon rather than the full logo artwork.
  return `https://www.google.com/s2/favicons?domain=${domain}&sz=256`;
}
